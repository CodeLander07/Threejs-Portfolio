import emailjs from '@emailjs/browser';
import { useRef, useState } from 'react';
import useAlert from '../hooks/useAlert.js';
import Alert from '../components/Alert.jsx';

const Contact = () => {
  const formRef = useRef();

  const { alert, showAlert, hideAlert } = useAlert();
  const [loading, setLoading] = useState(false);

  const [form, setForm] = useState({ name: '', email: '', message: '' });

  const handleChange = ({ target: { name, value } }) => {
    setForm({ ...form, [name]: value });
  };

  const handleSubmit = (e) => {
    e.preventDefault();
    setLoading(true);

    emailjs.sendForm(
      'service_yr2jq2j',
      'template_hyy50zi',
      formRef.current,
      'Onm6NOm5-iNlhxWQ-',
    )
      .then(
      () => {
          setLoading(false);
          showAlert({
            show: true,
            text: 'Thank you for your message ðŸ˜ƒ',
            type: 'success',
          });

          setForm({
            name: '',
            email: '',
            message: '',
          });

          setTimeout(() => {
            hideAlert();
          }, 3000);
        },
        (error) => {
          setLoading(false);
          console.error(error);

          showAlert({
            show: true,
            text: "I didn't receive your message ðŸ˜¢",
            type: 'danger',
          });
        },
      );
  };

  return (
    <section className="c-space my-20 m-[10rem]" id="contact">
      {alert.show && <Alert {...alert} />}

      <div className="relative min-h-screen flex items-center justify-center flex-col">
        <img src="/assets/terminal.png" alt="terminal-bg" className="absolute inset-0 min-h-screen hidden sm:block" />

        <div className="contact-container mt-4 p-4 sm:p-8">
          <h3 className="head-text mt-4 text-center sm:text-left">Let's talk</h3>

          <form ref={formRef} onSubmit={handleSubmit} className="mt-10 flex flex-col space-y-4 ">
            <label className="space-y-3">
              <span className="field-label">Full Name</span>
              <input
                type="text"
                name="name"
                value={form.name}
                onChange={handleChange}
                required
                className="field-input"
                placeholder="ex. John Doe"
              />
            </label>

            <label className="space-y-3">
              <span className="field-label">Email address</span>
              <input
                type="email"
                name="email"
                value={form.email}
                onChange={handleChange}
                required
                className="field-input"
                placeholder="ex. johndoe@gmail.com"
              />
            </label>

            <label className="space-y-3">
              <span className="field-label">Your message</span>
              <textarea
                name="message"
                value={form.message}
                onChange={handleChange}
                required
                rows={5}
                className="field-input "
                placeholder="Contact me about..."
              />
            </label>

            <button className="field-btn" type="submit" disabled={loading}>
              {loading ? 'Sending...' : 'Send Message'}

              <img src="/assets/arrow-up.png" alt="arrow-up" className="field-btn_arrow" />
            </button>
          </form>
        </div>
      </div>
    </section>
  );
};

export default Contact;
